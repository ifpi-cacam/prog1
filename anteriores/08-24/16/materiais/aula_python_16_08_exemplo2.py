"""
Comandos min() e max() em uma lista de inteiros/reais
"""
# min() - retorna o menor elemento da lista
# max() - retorna o maior elemento da lista

precos = [4.55, 3.76, 2.25, 1.89, 19.25, 11.5, 13.88]

print('Menor elemento da lista de precos: ', min(precos))
print('Maior elemento da lista de precos: ', max(precos))
